
package pojo;


public class Cidade {
    private int id;
    private String nome;
    private String estado;
    
    
    public int getId () {
        
        return id;
    }
    public String getNome () {
        
        return nome;
    }
    public String getEstado () {
        
        return estado;
    }
    public void setId (int id) {
        
        this.id = id;
    }
     public void setNome (String nome) {
        
        this.nome = nome;
    }
     public void setEstado (String estado) {
        
        this.estado = estado;
    }
}
